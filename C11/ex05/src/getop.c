#include "../include/ft.h"

int	get_op(char op)
{
	char	ops[5];
	int		i;

	i = 0;
	ops[0] = '+';
	ops[1] = '-';
	ops[2] = '*';
	ops[3] = '/';
	ops[4] = '%';
	while (i < 5)
	{
		if (op == ops[i])
			return (i);
		i++;
	}
	return (-1);
}
